-- IS_ACTIVE / MUST_CHANGE_PASSWORD use the native BOOLEAN type introduced in
-- Firebird 3.0. The %DB.TRUE% / %DB.FALSE% macros expand to the literal TRUE / FALSE
-- in the Firebird dialect (see TEFDBEngineType.GetBoolTrueLiteral in EF.DB.pas), so
-- the SQL queries from Kitto.Auth.DB / Kitto.AccessControl.DB work natively.
-- Apps that must run against Firebird 2.5 or earlier need to substitute SMALLINT here
-- and override the boolean macros via descendant TEFDBEngineType.
CREATE TABLE KITTO_USERS
(
  USER_NAME varchar(32) NOT NULL,
  PASSWORD_HASH varchar(60),
  IS_ACTIVE boolean DEFAULT TRUE NOT NULL,
  FIRST_NAME varchar(50),
  LAST_NAME varchar(50),
  EMAIL_ADDRESS varchar(100) NOT NULL,
  MUST_CHANGE_PASSWORD boolean DEFAULT FALSE NOT NULL,
  CONSTRAINT PK_KITTO_USERS PRIMARY KEY (USER_NAME)
);

