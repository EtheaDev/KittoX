CREATE VIEW ACTIVITY_BY_DATE (ACTIVITY_DATE, DURATION, ACTIVITIES)
AS  
SELECT
  A.ACTIVITY_DATE, 
  CAST(AVG(DATEDIFF(millisecond, A.START_TIME, A.END_TIME) / CAST(3600000 AS FLOAT)) AS DECIMAL(8,4)) DURATION, 
  COUNT(*) ACTIVITIES
FROM ACTIVITY A
GROUP BY
  ACTIVITY_DATE
;
-- Total hours per activity type, used by the Hours-by-Type pie on the
-- dashboard. SUM (not AVG) so the pie reflects share of total time per
-- type — AVG of per-activity duration is ~uniform across types and would
-- produce equal slices.
CREATE VIEW ACTIVITY_BY_TYPE (DURATION, TYPE_NAME)
AS
SELECT
  CAST(SUM(DATEDIFF(millisecond, A.START_TIME, A.END_TIME) / CAST(3600000 AS FLOAT)) AS DECIMAL(8,2)) DURATION,
  T.TYPE_NAME
FROM ACTIVITY A
join ACTIVITY_TYPE T on A.TYPE_ID = T.TYPE_ID
GROUP BY
  T.TYPE_NAME
;

-- Dashboard views — same shape as the SQL Server / PostgreSQL counterparts.
-- Firebird translation notes:
--   GETDATE()                 → CURRENT_DATE / CURRENT_TIMESTAMP
--   ISNULL(x, 0)              → COALESCE(x, 0)
--   DATEFROMPARTS(y,m,1)      → DATEADD(DAY, -(EXTRACT(DAY FROM CURRENT_DATE) - 1), CURRENT_DATE)
--   DATEADD(MONTH, 1, x)      → DATEADD(MONTH, 1, x)  (same)
--   DATEDIFF(MINUTE, s, e)    → DATEDIFF(MINUTE, s, e)  (same; works on TIME columns)

CREATE VIEW VW_ACTIVITY_BY_STATUS (STATUS, ACTIVITY_COUNT, TOTAL_HOURS) AS
SELECT
  A.STATUS,
  COUNT(*) AS ACTIVITY_COUNT,
  COALESCE(SUM(DATEDIFF(MINUTE, A.START_TIME, A.END_TIME) / 60.0), 0) AS TOTAL_HOURS
FROM ACTIVITY A
WHERE A.ACTIVITY_DATE >= DATEADD(DAY, -(EXTRACT(DAY FROM CURRENT_DATE) - 1), CURRENT_DATE)
  AND A.ACTIVITY_DATE <  DATEADD(MONTH, 1, DATEADD(DAY, -(EXTRACT(DAY FROM CURRENT_DATE) - 1), CURRENT_DATE))
GROUP BY A.STATUS
;

CREATE VIEW VW_ACTIVITY_DAILY_RANGE (ACTIVITY_DATE, ACTIVITY_COUNT, TOTAL_HOURS) AS
SELECT
  A.ACTIVITY_DATE,
  COUNT(*) AS ACTIVITY_COUNT,
  COALESCE(SUM(DATEDIFF(MINUTE, A.START_TIME, A.END_TIME) / 60.0), 0) AS TOTAL_HOURS
FROM ACTIVITY A
WHERE A.ACTIVITY_DATE >= DATEADD(DAY, -15, CURRENT_DATE)
  AND A.ACTIVITY_DATE <= DATEADD(DAY,  15, CURRENT_DATE)
GROUP BY A.ACTIVITY_DATE
;

CREATE VIEW VW_KPI_MONTHLY (ACTIVITY_COUNT, TOTAL_HOURS, ACTIVE_PROJECTS, ACTIVE_CUSTOMERS) AS
SELECT
  (SELECT COUNT(*) FROM ACTIVITY
   WHERE ACTIVITY_DATE >= DATEADD(DAY, -(EXTRACT(DAY FROM CURRENT_DATE) - 1), CURRENT_DATE)
     AND ACTIVITY_DATE <  DATEADD(MONTH, 1, DATEADD(DAY, -(EXTRACT(DAY FROM CURRENT_DATE) - 1), CURRENT_DATE))
  ) AS ACTIVITY_COUNT,
  (SELECT COALESCE(SUM(DATEDIFF(MINUTE, START_TIME, END_TIME) / 60.0), 0) FROM ACTIVITY
   WHERE ACTIVITY_DATE >= DATEADD(DAY, -(EXTRACT(DAY FROM CURRENT_DATE) - 1), CURRENT_DATE)
     AND ACTIVITY_DATE <  DATEADD(MONTH, 1, DATEADD(DAY, -(EXTRACT(DAY FROM CURRENT_DATE) - 1), CURRENT_DATE))
  ) AS TOTAL_HOURS,
  (SELECT COUNT(*) FROM PROJECT WHERE STATUS = 'Open') AS ACTIVE_PROJECTS,
  (SELECT COUNT(DISTINCT P.CUSTOMER_ID)
   FROM ACTIVITY A
   INNER JOIN PHASE PH ON PH.PHASE_ID = A.PHASE_ID
   INNER JOIN PROJECT P ON P.PROJECT_ID = PH.PROJECT_ID
   WHERE A.ACTIVITY_DATE >= DATEADD(DAY, -(EXTRACT(DAY FROM CURRENT_DATE) - 1), CURRENT_DATE)
     AND A.ACTIVITY_DATE <  DATEADD(MONTH, 1, DATEADD(DAY, -(EXTRACT(DAY FROM CURRENT_DATE) - 1), CURRENT_DATE))
  ) AS ACTIVE_CUSTOMERS
FROM RDB$DATABASE
;

