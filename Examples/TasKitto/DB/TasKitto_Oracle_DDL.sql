/********************* VIEWS **********************/

-- Average activity duration (hours) per day.
CREATE VIEW ACTIVITY_BY_DATE (ACTIVITY_DATE, DURATION, ACTIVITIES) AS
SELECT
  A.ACTIVITY_DATE,
  CAST(AVG((A.END_TIME - A.START_TIME) * 24) AS NUMBER(8,4)) AS DURATION,
  COUNT(*) AS ACTIVITIES
FROM ACTIVITY A
GROUP BY A.ACTIVITY_DATE;

-- Total hours per activity type, used by the Hours-by-Type pie on the
-- dashboard. SUM (not AVG) so the pie reflects share of total time per type.
CREATE VIEW ACTIVITY_BY_TYPE (DURATION, TYPE_NAME) AS
SELECT
  CAST(SUM((A.END_TIME - A.START_TIME) * 24) AS NUMBER(8,2)) AS DURATION,
  T.TYPE_NAME
FROM ACTIVITY A
JOIN ACTIVITY_TYPE T ON A.TYPE_ID = T.TYPE_ID
GROUP BY T.TYPE_NAME;

-- Current-month activity count and hours by status.
CREATE VIEW VW_ACTIVITY_BY_STATUS (STATUS, ACTIVITY_COUNT, TOTAL_HOURS) AS
SELECT
  A.STATUS,
  COUNT(*) AS ACTIVITY_COUNT,
  NVL(SUM((A.END_TIME - A.START_TIME) * 24), 0) AS TOTAL_HOURS
FROM ACTIVITY A
WHERE A.ACTIVITY_DATE >= TRUNC(SYSDATE, 'MM')
  AND A.ACTIVITY_DATE <  ADD_MONTHS(TRUNC(SYSDATE, 'MM'), 1)
GROUP BY A.STATUS;

-- Activity count and hours per day over a +/-15 day window around today.
CREATE VIEW VW_ACTIVITY_DAILY_RANGE (ACTIVITY_DATE, ACTIVITY_COUNT, TOTAL_HOURS) AS
SELECT
  A.ACTIVITY_DATE,
  COUNT(*) AS ACTIVITY_COUNT,
  NVL(SUM((A.END_TIME - A.START_TIME) * 24), 0) AS TOTAL_HOURS
FROM ACTIVITY A
WHERE A.ACTIVITY_DATE >= TRUNC(SYSDATE) - 15
  AND A.ACTIVITY_DATE <= TRUNC(SYSDATE) + 15
GROUP BY A.ACTIVITY_DATE;

-- Single-row KPI card values for the current month.
CREATE VIEW VW_KPI_MONTHLY (ACTIVITY_COUNT, TOTAL_HOURS, ACTIVE_PROJECTS, ACTIVE_CUSTOMERS) AS
SELECT
  (SELECT COUNT(*) FROM ACTIVITY
   WHERE ACTIVITY_DATE >= TRUNC(SYSDATE, 'MM')
     AND ACTIVITY_DATE <  ADD_MONTHS(TRUNC(SYSDATE, 'MM'), 1)
  ) AS ACTIVITY_COUNT,
  (SELECT NVL(SUM((END_TIME - START_TIME) * 24), 0) FROM ACTIVITY
   WHERE ACTIVITY_DATE >= TRUNC(SYSDATE, 'MM')
     AND ACTIVITY_DATE <  ADD_MONTHS(TRUNC(SYSDATE, 'MM'), 1)
  ) AS TOTAL_HOURS,
  (SELECT COUNT(*) FROM PROJECT WHERE STATUS = 'Open') AS ACTIVE_PROJECTS,
  (SELECT COUNT(DISTINCT P.CUSTOMER_ID)
   FROM ACTIVITY A
   INNER JOIN PHASE PH ON PH.PHASE_ID = A.PHASE_ID
   INNER JOIN PROJECT P ON P.PROJECT_ID = PH.PROJECT_ID
   WHERE A.ACTIVITY_DATE >= TRUNC(SYSDATE, 'MM')
     AND A.ACTIVITY_DATE <  ADD_MONTHS(TRUNC(SYSDATE, 'MM'), 1)
  ) AS ACTIVE_CUSTOMERS
FROM DUAL;
