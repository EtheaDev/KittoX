-- Oracle 19c DDL script for the TasKitto example (tables + foreign keys + views).
--
-- Dialect notes (see TEFOracleDBEngineType in Source/EF/EF.DB.pas):
--   * Boolean columns (IS_ACTIVE / MUST_CHANGE_PASSWORD) use NUMBER(1) with 1/0
--     (Oracle pre-23c has no native BOOLEAN). The %DB.TRUE% / %DB.FALSE% macros
--     expand to 1 / 0 on Oracle, so Kitto.Auth.DB / Kitto.AccessControl.DB SQL
--     works natively.
--   * 'time' (START_TIME / END_TIME) has no Oracle equivalent: it is stored in a
--     DATE holding only the time-of-day (KittoX ExpandDateTimeFrom).
--   * varchar -> VARCHAR2; integer -> NUMBER(10); float -> FLOAT.
--   * Identifiers are unquoted (Oracle folds them to upper case, matching the
--     metadata). Recommended DB character set: AL32UTF8.
--
-- Dashboard views translate the SQL Server / PostgreSQL date functions to Oracle:
--   GETDATE() / CURRENT_DATE          -> SYSDATE (TRUNC(SYSDATE) for date only)
--   DATEFROMPARTS(YEAR,MONTH,1)       -> TRUNC(SYSDATE,'MM')  (first day of month)
--   DATEADD(MONTH, 1, x)              -> ADD_MONTHS(x, 1)
--   DATEADD(DAY, n, x)                -> x + n
--   DATEDIFF(MINUTE, s, e) / 60       -> (e - s) * 24     (DATE diff = fractional days)
--   ISNULL(x, 0)                      -> NVL(x, 0)
--   subquery with no FROM             -> FROM DUAL
--
-- Primary keys are string ids (no auto-increment), so no sequences/identity are
-- needed. Run this as the schema owner, then run TasKitto_Oracle_Data.sql.

/******************** TABLES **********************/

CREATE TABLE KITTO_USERS
(
  USER_NAME            VARCHAR2(32)  NOT NULL,
  PASSWORD_HASH        VARCHAR2(60),
  IS_ACTIVE            NUMBER(1) DEFAULT 1 NOT NULL CHECK (IS_ACTIVE IN (0, 1)),
  FIRST_NAME           VARCHAR2(50),
  LAST_NAME            VARCHAR2(50),
  EMAIL_ADDRESS        VARCHAR2(100) NOT NULL,
  MUST_CHANGE_PASSWORD NUMBER(1) DEFAULT 0 NOT NULL CHECK (MUST_CHANGE_PASSWORD IN (0, 1)),
  CONSTRAINT PK_KITTO_USERS PRIMARY KEY (USER_NAME)
);

